package co.jp.swar.form;

public class MonsterForm {
	private int id;
	private String jname;
	private String kname;
	private int hp;
	private Integer atk;
	private Integer def;
	private Integer spd;
	private Integer criticalRate;
	private Integer criticalDamage;
	private Integer resist;
	private Integer accuracy;
	
	/**
	 * @return id
	 */
	public int getId() {
		return id;
	}
	/**
	 * @return name
	 */
	public String getJname() {
		return jname;
	}
	/**
	 * @return kname
	 */
	public String getKname() {
		return kname;
	}
	/**
	 * @param id セットする id
	 */
	public void setId(int id) {
		this.id = id;
	}
	/**
	 * @param name セットする name
	 */
	public void setJname(String jname) {
		this.jname = jname;
	}
	/**
	 * @param kname セットする kname
	 */
	public void setKname(String kname) {
		this.kname = kname;
	}
	public int getHp() {
		return hp;
	}
	public void setHp(int hp) {
		this.hp = hp;
	}
	/**
	 * @return atk
	 */
	public Integer getAtk() {
		return atk;
	}
	/**
	 * @return def
	 */
	public Integer getDef() {
		return def;
	}
	/**
	 * @return spd
	 */
	public Integer getSpd() {
		return spd;
	}
	/**
	 * @return criticalRate
	 */
	public Integer getCriticalRate() {
		return criticalRate;
	}
	/**
	 * @return criticalDamage
	 */
	public Integer getCriticalDamage() {
		return criticalDamage;
	}
	/**
	 * @return resist
	 */
	public Integer getResist() {
		return resist;
	}
	/**
	 * @return accuracy
	 */
	public Integer getAccuracy() {
		return accuracy;
	}
	/**
	 * @param atk セットする atk
	 */
	public void setAtk(Integer atk) {
		this.atk = atk;
	}
	/**
	 * @param def セットする def
	 */
	public void setDef(Integer def) {
		this.def = def;
	}
	/**
	 * @param spd セットする spd
	 */
	public void setSpd(Integer spd) {
		this.spd = spd;
	}
	/**
	 * @param criticalRate セットする criticalRate
	 */
	public void setCriticalRate(Integer criticalRate) {
		this.criticalRate = criticalRate;
	}
	/**
	 * @param criticalDamage セットする criticalDamage
	 */
	public void setCriticalDamage(Integer criticalDamage) {
		this.criticalDamage = criticalDamage;
	}
	/**
	 * @param resist セットする resist
	 */
	public void setResist(Integer resist) {
		this.resist = resist;
	}
	/**
	 * @param accuracy セットする accuracy
	 */
	public void setAccuracy(Integer accuracy) {
		this.accuracy = accuracy;
	}
	
	
}
